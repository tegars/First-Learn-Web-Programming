//ini menggunakan for
var angka=1;
for(a=angka; a<=3; a++){
	document.write(a+'<br/>');
}
document.write('<hr/>');


//menggunakan while
var a=angka;
while(a<=3){
	document.write(a+'<br/>');
	a++;
}
document.write('<hr/>');


//menggunakan do while
var a=angka;
do{
	document.write(a+'<br/>');
	a++;
}while(a<=3)
document.write('<hr/>');


